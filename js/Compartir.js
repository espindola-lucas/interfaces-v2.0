
let click1 = 'si';
function Seleccionar1 (){
    if (click1 == 'si'){
        document.getElementById('box1').style.background= " #E4D9E4";
        return   click1 = 'no';
    }
    if (click1 == 'no'){
        document.getElementById('box1').style.background= 'white';
        return click1 = 'si';
    }
    
}
